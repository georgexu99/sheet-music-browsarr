async fn search(
    State(state): State<AppState>,
    headers: HeaderMap,
    // Vec<(String, String)> preserves repeated query keys (HTMX serialises
    // the `sources` checkbox group as `?sources=imslp&sources=mutopia`),
    // which a HashMap would clobber. Single-value params (`q`, `instrument`)
    // are read out via first_param().
    Query(params): Query<Vec<(String, String)>>,
) -> Response {
    let query = first_param(&params, "q")
        .unwrap_or_default()
        .trim()
        .to_string();
    let instrument = first_param(&params, "instrument")
        .filter(|s| !s.is_empty())
        .and_then(Instrument::from_slug);
    let difficulty = first_param(&params, "difficulty")
        .filter(|s| !s.is_empty())
        .and_then(Difficulty::from_slug);
    let score_type = first_param(&params, "score_type")
        .filter(|s| !s.is_empty())
        .and_then(ScoreType::from_slug);
    // Toggle param accepts "1" or "true" as truthy; anything else is off.
    let public_domain_only = first_param(&params, "pd_only")
        .is_some_and(|s| s == "1" || s.eq_ignore_ascii_case("true"));
    let source_filter = parse_source_filter(&params);
    let filters = SearchFilters {
        instrument,
        difficulty,
        public_domain_only,
        score_type,
    };

    let is_htmx = headers.get("hx-request").is_some();
    let ip = audit::client_ip(&headers);
    let ua = audit::user_agent(&headers);

    // 1-based page index. Out-of-range / unparsable / over-cap inputs fall
    // back to page 1; we'd rather show something than 4xx the user.
    let page: u32 = first_param(&params, "page")
        .and_then(|s| s.parse::<u32>().ok())
        .filter(|p| *p >= 1 && *p <= SEARCH_MAX_PAGE)
        .unwrap_or(1);

    // Infinite-scroll append mode: triggered by the sentinel `<li>` at the
    // bottom of the current results list. Returns just *this* page's new
    // items (sliced from the cumulative dedup) wrapped in a bare partial
    // that gets spliced into the existing list via hx-swap="outerHTML".
    // Gated on the HTMX request header so hand-typed `?append=1` URLs
    // gracefully render the full page instead of a bare fragment.
    let is_append =
        is_htmx && first_param(&params, "append").is_some_and(|s| !s.is_empty());

    if query.is_empty() {
        audit::record(
            &state.pool,
            &ip,
            ua.as_deref(),
            "search",
            None,
            "empty_query",
            None,
        )
        .await;
        if is_append {
            return empty_append(&query);
        }
        return render_response(
            is_htmx,
            &state,
            &query,
            Vec::new(),
            None,
            source_filter.as_ref(),
            &filters,
        );
    }
    if query.chars().count() < SEARCH_MIN_QUERY_LEN {
        // Typeahead: too short to be useful and the upstream sources will
        // mostly return noise. Return an empty result set silently — the
        // user will keep typing.
        if is_append {
            return empty_append(&query);
        }
        return render_response(
            is_htmx,
            &state,
            &query,
            Vec::new(),
            None,
            source_filter.as_ref(),
            &filters,
        );
    }

    // Multilingual expansion: a single user query becomes up to 4 query
    // variants (English / Simplified / Traditional / Pinyin) when known
    // composer or instrument names are recognised. See src/i18n/alias.rs.
    let variants = i18n::expand_query(&query);

    // Source-selection narrows the fan-out at the route layer — disabled
    // sources never get a request fired, so it stays cheap. Default
    // (`source_filter = None`) means all sources are enabled.
    let selected_sources: Vec<Arc<dyn Source>> = state
        .sources
        .iter()
        .filter(|s| {
            source_filter
                .as_ref()
                .map(|f| f.contains(s.id()))
                .unwrap_or(true)
        })
        .cloned()
        .collect();

    // Cross-product: (source, variant) -> one parallel search future.
    // Failing futures get a logged warn + empty Vec; nothing poisons the
    // whole search.
    let pairs: Vec<(Arc<dyn Source>, String)> = selected_sources
        .iter()
        .flat_map(|s| {
            let src = s.clone();
            variants.iter().map(move |v| (src.clone(), v.clone()))
        })
        .collect();
    let search_cache: SearchCache = state.search_cache.clone();
    let health_map = state.source_health.clone();
    // Bump the per-source limit linearly with page number. The cache key in
    // src/cache.rs now includes `limit`, so each page's fetch caches under
    // its own slot — page 1's 10-per-source vec doesn't shadow page 2's
    // 20-per-source vec.
    let per_source_limit = SEARCH_LIMIT_PER_SOURCE * (page as usize);
    let futures = pairs.into_iter().map(|(src, v)| {
        let cache = search_cache.clone();
        let health = health_map.clone();
        let f = filters.clone();
        async move {
            match cache::cached_search(&cache, &src, &v, &f, per_source_limit).await {
                Ok(rs) => {
                    // Cache hits also count as "ok" — if the cache has a
                    // valid entry, the source was reachable within the last
                    // TTL (60s), which is good enough for liveness.
                    health::record_ok(&health, src.id());
                    rs
                }
                Err(e) => {
                    // `{:#}` flattens an anyhow chain into a single
                    // colon-separated line, which keeps the structured
                    // log readable while still surfacing the underlying
                    // cause ("flaresolverr search …: flaresolverr
                    // returned status=error message=…: …" instead of
                    // just "flaresolverr search …").
                    let chain = format!("{:#}", e);
                    tracing::warn!(
                        source = src.id(),
                        variant = %v,
                        error = %chain,
                        "source search failed"
                    );
                    health::record_err(&health, src.id(), &chain);
                    Vec::new()
                }
            }
        }
    });
    let groups = join_all(futures).await;

    // Dedupe across (source, id) — the same work commonly surfaces via
    // multiple variants (e.g. "Chopin" and "肖邦" both return it).
    let mut seen = HashSet::new();
    let mut results = Vec::new();
    for group in groups {
        for r in group {
            if seen.insert((r.source.clone(), r.id.clone())) {
                results.push(r);
            }
        }
    }
    let deduped_total = results.len();

    // Post-hoc filters (difficulty / pd_only / score_type). Applied
    // *before* the page-target truncate so pagination semantics stay
    // sane: page 1 of "Beginner only" returns the first 20 Beginner
    // results, not "the 20 results that happened to land in page 1
    // before filtering took out 18 of them". `instrument` was already
    // applied upstream at the source layer.
    results.retain(|r| passes_post_hoc(r, &filters));

    // Page-1 shows the first SEARCH_PAGE_SIZE; page-N shows the first
    // N * SEARCH_PAGE_SIZE. We always serve the cumulative slice (the
    // "Load more" button replaces the whole `#results` div), which keeps
    // back/forward navigation and full-page reloads of `?page=N` honest.
    let target_count = (page as usize).saturating_mul(SEARCH_PAGE_SIZE);
    if results.len() > target_count {
        results.truncate(target_count);
    }

    // Surface "Load more" only when we filled the requested page exactly
    // *and* we haven't yet hit the safety cap. If dedup left us short the
    // upstream sources have plausibly run out of new (source,id) hits;
    // exposing the button would just produce an empty next page.
    let next_page = if results.len() >= target_count && page < SEARCH_MAX_PAGE {
        Some(page + 1)
    } else {
        None
    };

    audit::record(
        &state.pool,
        &ip,
        ua.as_deref(),
        "search",
        Some(&query),
        "ok",
        Some(&format!(
            r#"{{"results":{},"variants":{},"page":{},"deduped":{}}}"#,
            results.len(),
            variants.len(),
            page,
            deduped_total,
        )),
    )
    .await;

    // No Cache-Control header on /search responses. The same URL serves
    // two different shapes (HTMX partial vs full page on native nav) and
    // browsers cache by URL only, so caching causes a partial to be
    // rendered as a full document after pressing Enter. The server-side
    // moka cache (`src/cache.rs`) already absorbs cross-user repeat
    // queries; the browser-side cache would only deduplicate same-user
    // self-repeat which isn't worth the partial-vs-full collision.
    if is_append {
        // Split off just this page's new items. The first
        // `(page-1)*PAGE_SIZE` items were already shown on earlier
        // scroll triggers; the append partial only needs the delta.
        let start = (page as usize).saturating_sub(1) * SEARCH_PAGE_SIZE;
        let new_items = if results.len() > start {
            results.split_off(start)
        } else {
            Vec::new()
        };
        return ResultsAppendPartial {
            query,
            results: new_items,
            next_page,
        }
        .into_response();
    }
    render_response(
        is_htmx,
        &state,
        &query,
        results,
        next_page,
        source_filter.as_ref(),
        &filters,
    )
}

/// Bare append partial with no items + no sentinel. Used for the
/// degenerate empty/short-query case so a sentinel-fired request still
/// gets a clean response shape rather than a render of the full page.
fn empty_append(query: &str) -> Response {
    ResultsAppendPartial {
        query: query.to_string(),
        results: Vec::new(),
        next_page: None,
    }
    .into_response()
}

/// HTMX requests get the results-only partial (filter controls live outside
/// the swap target so they don't need re-rendering); full-page navigations
/// render the search shell with filter state populated.
fn render_response(
    is_htmx: bool,
    state: &AppState,
    query: &str,
    results: Vec<SearchResult>,
    next_page: Option<u32>,
    source_filter: Option<&HashSet<String>>,
    filters: &SearchFilters,
) -> Response {
    if is_htmx {
        ResultsPartial {
            query: query.to_string(),
            results,
            next_page,
        }
        .into_response()
    } else {
        let ui = build_filter_options(state, source_filter, filters);
        let selected_source_count = ui.sources.iter().filter(|s| s.selected).count();
        // "Active" = anything diverging from the home-page default (all
        // sources ticked, no dropdown selection, PD-only off). Drives
        // the Reset-filters link visibility.
        let has_active_filters = selected_source_count < ui.sources.len()
            || filters.instrument.is_some()
            || filters.difficulty.is_some()
            || filters.score_type.is_some()
            || filters.public_domain_only;
        SearchPage {
            query: query.to_string(),
            results,
            message: None,
            next_page,
            selected_source_count,
            has_active_filters,
            public_domain_only: filters.public_domain_only,
            sources: ui.sources,
            instruments: ui.instruments,
            difficulties: ui.difficulties,
            score_types: ui.score_types,
        }
        .into_response()
    }
}

async fn pdf_handler(
    State(state): State<AppState>,
    headers: HeaderMap,
    Path((source_id, id)): Path<(String, String)>,
) -> Response {
    let ip = audit::client_ip(&headers);
    let ua = audit::user_agent(&headers);

    let source = match state.find_source(&source_id) {
        Some(s) => s,
        None => {
            audit::record(
                &state.pool,
                &ip,
                ua.as_deref(),
                "pdf",
                Some(&format!("{source_id}/{id}")),
                "unknown_source",
                None,
            )
            .await;
            return (axum::http::StatusCode::NOT_FOUND, "Unknown source").into_response();
        }
    };

    match source.fetch_pdf_bytes(&id, MAX_PDF_BYTES).await {
        Ok(bytes) => {
            health::record_ok(&state.source_health, source.id());
            audit::record(
                &state.pool,
                &ip,
                ua.as_deref(),
                "pdf",
                Some(&format!("{source_id}/{id}")),
                "ok",
                Some(&format!(r#"{{"bytes":{}}}"#, bytes.len())),
            )
            .await;
            let mut out = HeaderMap::new();
            out.insert(header::CONTENT_TYPE, HeaderValue::from_static("application/pdf"));
            let fname = sanitize_filename(&id);
            if let Ok(v) = HeaderValue::from_str(&format!(r#"inline; filename="{fname}.pdf""#)) {
                out.insert(header::CONTENT_DISPOSITION, v);
            }
            (out, bytes).into_response()
        }
        Err(e) => {
            tracing::warn!(
                source = %source_id,
                id = %id,
                error = %e,
                "fetch_pdf_bytes failed; falling back to external_url"
            );
            health::record_err(&state.source_health, source.id(), &e.to_string());
            audit::record(
                &state.pool,
                &ip,
                ua.as_deref(),
                "pdf",
                Some(&format!("{source_id}/{id}")),
                "fetch_failed_redirect",
                None,
            )
            .await;
            Redirect::to(&source.external_url(&id)).into_response()
        }
    }
}

fn sanitize_filename(s: &str) -> String {
    s.chars()
        .map(|c| match c {
            'A'..='Z' | 'a'..='z' | '0'..='9' | '.' | '-' | '_' => c,
            _ => '_',
        })
        .collect()
}

async fn login_page() -> impl IntoResponse {
    LoginTemplate { error: None }
}

#[derive(Deserialize)]
struct LoginForm {
    password: String,
}

async fn login_submit(
    State(state): State<AppState>,
    session: Session,
    Form(form): Form<LoginForm>,
) -> impl IntoResponse {
    match auth::verify_admin(&state.pool, &form.password).await {
        Ok(true) => {
            if let Err(e) = session.insert("admin", true).await {
                tracing::warn!(error = %e, "session insert failed");
            }
            Redirect::to("/admin").into_response()
        }
        Ok(false) => LoginTemplate {
            error: Some("Invalid password".into()),
        }
        .into_response(),
        Err(e) => {
            tracing::error!(error = %e, "verify_admin failed");
            LoginTemplate {
                error: Some("Internal error".into()),
            }
            .into_response()
        }
    }
}

async fn logout(session: Session) -> impl IntoResponse {
    let _ = session.flush().await;
    Redirect::to("/login")
}
