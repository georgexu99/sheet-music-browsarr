const MAX_PDF_BYTES: usize = 10 * 1024 * 1024;
/// How many results to ask each upstream source for, on page 1. Set to 20
/// to match MuseScore's natural search-page size (their /sheetmusic page
/// always renders 20 cards, capped by our scraper's `.take(limit)` — so
/// limit=10 was discarding half the cards we'd already fetched for free).
/// IMSLP's OpenSearch and Mutopia's CGI both handle limit=20 with no extra
/// upstream cost (single HTTP call, larger response slice). Page N then
/// fans out to `SEARCH_LIMIT_PER_SOURCE * N` per source, matching the
/// cumulative target_count.
const SEARCH_LIMIT_PER_SOURCE: usize = 20;
/// Target page size for the unified, post-dedup result list. Each page N
/// shows results 1..(N * SEARCH_PAGE_SIZE) (the simple full-replace
/// pagination flow — see `search_results.html`). 20 keeps the badge-laden
/// cards from overflowing the viewport on first paint while still showing
/// noticeably more than a single source's worth.
const SEARCH_PAGE_SIZE: usize = 20;
/// Hard upper bound on `?page=N`. Bounds both upstream per-source `limit`
/// and the post-dedup slice; without it a malicious request could ask for
/// page=10000 and we'd happily fan out limit=100000 per source.
const SEARCH_MAX_PAGE: u32 = 10;

/// Tailwind-compiled stylesheet. Generated by the Docker builder (or by
/// build.rs as an empty stub for local cargo check). Served by `/static/app.css`.
const APP_CSS: &str = include_str!("../../dist/styles.css");
/// Typeahead: ignore queries shorter than this. Avoids 1-char query storms
/// while users are still typing the start of a name.
const SEARCH_MIN_QUERY_LEN: usize = 2;

#[derive(Template)]
#[template(path = "search.html")]
struct SearchPage {
    query: String,
    results: Vec<SearchResult>,
    message: Option<String>,
    /// `Some(n)` when there is likely more to fetch — the trimmed result
    /// list filled the requested page exactly. `None` once we know we've
    /// hit the end (or pagination is otherwise unavailable, e.g. empty
    /// query). The template renders an HTMX "Load more" button only when
    /// `Some`. `SearchPage` and `ResultsPartial` share the partial via
    /// `{% include %}`, so their context shapes must match.
    next_page: Option<u32>,
    sources: Vec<SourceFilterOption>,
    instruments: Vec<FilterOption>,
    difficulties: Vec<FilterOption>,
    score_types: Vec<FilterOption>,
    /// Current state of the public-domain-only toggle. Distinct from the
    /// dropdowns because there's no list of options — it's a single
    /// checkbox chip.
    public_domain_only: bool,
    /// How many source checkboxes are currently ticked. Drives the count
    /// badge next to "Sources ▾" in the filter dropdown ("Sources [2]"
    /// when not all are selected). Live-updated by a small JS in
    /// search.html on checkbox change since the HTMX swap only updates
    /// `#results`, not the filter UI.
    selected_source_count: usize,
    /// True iff any filter is set to a non-default value (any dropdown
    /// non-default, any source unticked, or PD-only on). Drives the
    /// visibility of the "Reset filters" link.
    has_active_filters: bool,
}

